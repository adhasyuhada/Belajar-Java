public class ForLoop{
	public static void main(String[] args){
	//membuat variabel
	String nama = "Hasan";
	int umur = 26;

	//membuat perulangan for (nilai awal; perbandingan perulangan; penambahan perulangan;)
	for(int i = 0; i < 4; i++ ){
		System.out.println("Nama saya " + nama + " Umur saya " + umur);
	}
	}
}
